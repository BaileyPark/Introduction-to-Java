package variable.ex.operator;

public class Operator1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;

        int sum = a + b;
        int diff = a - b;
        int multi = a * b;
        int div = a / b;
        int mod = a % b;

        System.out.println(sum);
        System.out.println(diff);
        System.out.println(multi);
        System.out.println(div);
        System.out.println(mod);
    }
}
